// 验证是否为正整数
export function formatter(val) {
  if (val.length === 1) {
    val = val.replace(/[^\d]/g, '').replace(/^0{1,}/g, '')
  } else {
    val = val.replace(/\D/g, '').replace(/^0{1,}/g, '')
  }
  return val
}

